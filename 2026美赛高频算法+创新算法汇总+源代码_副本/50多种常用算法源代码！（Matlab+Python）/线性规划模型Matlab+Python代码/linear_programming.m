% 线性规划模型案例说明：
% 本案例使用线性规划解决生产资源分配问题。某工厂生产两种产品A和B，
% 每种产品需要消耗原材料和工时，有最大供应量和市场需求限制。
% 目标是最大化总利润。线性规划适用于目标函数和约束条件均为线性的问题。

% 目标函数：最大化总利润 = 50x1 + 60x2
% 转化为最小化问题：min -50x1 -60x2
f = [-50; -60];

% 不等式约束：A*x <= b
A = [2, 3;    % 原材料约束：2x1 + 3x2 <= 100
     4, 2;    % 工时约束：4x1 + 2x2 <= 120
     1, 0;    % 产品A需求约束：x1 <= 25
     0, 1];   % 产品B需求约束：x2 <= 30
b = [100; 120; 25; 30];

% 等式约束：无
Aeq = [];
beq = [];

% 变量下界：x1 >= 0, x2 >= 0
lb = [0; 0];
ub = [];

% 求解线性规划
[x, fval] = linprog(f, A, b, Aeq, beq, lb, ub);

% 输出结果
fprintf('求解状态: 成功\n');
fprintf('最优解: 产品A生产 %.2f 件, 产品B生产 %.2f 件\n', x(1), x(2));
fprintf('最大总利润: %.2f 元\n', -fval);

% 计算各约束条件的使用情况
constraint_values = A * x;
fprintf('\n约束条件使用情况:\n');
fprintf('原材料约束: 实际使用 %.2f, 限制 %.2f\n', constraint_values(1), b(1));
fprintf('工时约束: 实际使用 %.2f, 限制 %.2f\n', constraint_values(2), b(2));
fprintf('产品A需求约束: 实际使用 %.2f, 限制 %.2f\n', constraint_values(3), b(3));
fprintf('产品B需求约束: 实际使用 %.2f, 限制 %.2f\n', constraint_values(4), b(4));
